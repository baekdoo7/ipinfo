                        <div class="par"><strong>옵티머스·무역금융·DLS 펀드 줄줄이 환매 중단...피해 규모는 </strong><br><br>30일 금융 업계에 따르면 최근 옵티머스 자산운용이 판매하던 사모펀드의 환매가 중단됐다. 옵티머스자산운용은 공공기관 매출채권에 투자한다는 명목으로 지난 3년여간 NH투자증권(4528억원)과 한국투자증권(407억원) 등 판매사를 통해 투자자를 끌어 모았다. <br><br>개인 투자자들은 연 3% 수익률을 지급한다는 말만 믿고 수억원을 투자했다. 하지만 환원금도 돌려받지 못하는 사태에 이르렀다. 피해 규모는 최대 5000억원으로 투자금 전액이 환매 중단될 수 있다는 우려가 나온다.<br><br>사모펀드 피해는 지난해 말부터 잇따라 터져 나왔다. 지난해 라임자산운용(대신증권·부산은행·신한금투·신한은행·우리은행·하나은행) 1조6679억원과 하나은행 해외금리 연계 파생결합펀드(DLF) 7950억원 손실 사태 등을 시작으로 올해 ‘제2의 라임’으로 불리는 옵티머스 공공기관 매출채권 펀드는 NH투자증권(4778억원), 한국투자증권(577억원), 케이프투자증권(146억원) 등 약 5000억원이 환매가 중단됐다.<br><br>또 KB증권이 판매한 1000억원 규모의 무역금융펀드 파생결합증권(DLS)은 4월 만기 상환을 지키지 못하고 3개월 환매를 늦췄다. 싱가포르 트랜스아시아(TA)가 운용을 담당하고 NH투자증권이 발행을 맡은 이 상품은 무역업체들의 금융채권에 투자해 수익을 내는 구조다. 무역거래가 정상적으로 이뤄지면 연 4%대 수익을 낼 수 있지만, 코로나19로 세계 무역이 위축되면서 결국 환매가 중단됐다.<br><br>플랫폼 파트너스 자산운용의 무역금융펀드인 '더플랫폼 아시아무역금융 1Y' 2호, 3호, 4호 환매도 중단됐다. 이 역시 신종 코로나바이러스 감염증(코로나19) 사태로 글로벌 무역이 위축되면서 투자금 회수에 문제가 발생한 것으로 알려졌다. 우리은행 등이 판매한 이 펀드의 환매 중단 규모는 500억원 쯤으로 추정된다.<br><br><strong>금융위·금감원, 사모펀드 1만개 '전수조사' 카드 먹힐까</strong><br><br>사태의 심각성을 느낀 금융당국은 1만여개에 달하는 사모펀드를 전수 조사하겠다고 나섰다. 지난해 국내 헤지펀드 1위 업체 라임자산운용의 1조7000억원 환매 중단 사태가 채 수습되지 않은 채 디스커버리·옵티머스·무역금융펀드까지 수 천억원에 달하는 환매 중단이 잇따르자 사모펀드 시장 전체 운용 실태를 살펴보겠다는 것이다.<br><br>은성수 금융위원장은 6월 23일 "사모펀드 신뢰가 무너지면 자본시장 전체에 신뢰가 무너지기 때문에 시간이 걸리더라도 1만여개의 사모펀드를 전부 점검해봐야 한다"며 전수 조사 이유를 밝혔다.<br><br>금융 당국은 이르면 이번 주 중 사모펀드 전수 조사 방식과 일정 등을 논의하는 합동 점검 회의를 개최한다. 자산운용사와 판매사, 그리고 주식을 보관하고 매매를 담당하는 수탁사, 펀드 기준가와 수익률을 산정하는 사무관리사의 자산, 서류 내역이 일치하는지 확인하는 4자 교차 점검 방식이 유력하다.<br><br>금감원 노조와 피해자대책위는 '겉핥기식 대책'이라며 회의적인 반응이다. 금감원 노동조합은 "5개 팀, 32명에 불과한 자산운용검사국이 1만개가 넘는 펀드를 정밀검사하려면 수십 년은 걸릴 것이다"라고 비판했다.<br><br><strong>무분별한 규제완화가 사태 키웠다 주장</strong><br><br>사모펀드 피해자공동대책위는 사모펀드 사태의 1차 책임이 당국의 무분별한 규제완화 조치에 있다고 질타한다. 2015년 금융위에서 전문투자형 사모펀드 최소 투자금을  5억원에서 1억원으로 낮추고, 사모펀드 운용사의 요건을 인가제에서 등록제로 완화했기 때문이다.<br><br>진입 문턱이 낮아지자, 개인 투자자들이 사모펀드에 대거 몰려들었고 공모펀드가 아닌 전문투자형 펀드임에도 투자자 성향을 고려하지 않은채 불완전 상품판매가 이어졌다는 주장이다. <br><br>김득의 금융정의연대 대표는 "사모펀드